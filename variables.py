# As in any other language, variables can have types. You don't declare them directly, though.

# Strings
myName = "Ty"
programmingLanguage = "Python 3"

# Ints
age = 15

# Floats / doubles.
gpa = 3.79

# Lists.
# Note that lists can contain any type.
knownLanguages = ["Python", "Java", "C#", "JavaScript"]

# Dictionaries.
testGrades = {
	"Andy": "A-",
	"Robby": "B+"
}

# Booleans
isTall = False
isBlind = True

# None type (like NULL in C).
vision = None
